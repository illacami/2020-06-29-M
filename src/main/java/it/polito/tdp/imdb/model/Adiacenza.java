package it.polito.tdp.imdb.model;

public class Adiacenza {

	private int peso;
	private int d1;
	private int d2;
	
	public Adiacenza(int peso, int d1, int d2) {
		super();
		this.peso = peso;
		this.d1 = d1;
		this.d2 = d2;
	}

	public int getPeso() {
		return peso;
	}

	public void setPeso(int peso) {
		this.peso = peso;
	}

	public int getD1() {
		return d1;
	}

	public void setD1(int d1) {
		this.d1 = d1;
	}

	public int getD2() {
		return d2;
	}

	public void setD2(int d2) {
		this.d2 = d2;
	}
	
	
	
	
}
